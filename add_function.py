def add(a, b):
    """
    Add two numbers together.

    Args:
        a: First number
        b: Second number

    Returns:
        The sum of a and b
    """
    return a + b


# Example usage
if __name__ == "__main__":
    result = add(3, 5)
    print(f"3 + 5 = {result}")

    result = add(10.5, 20.3)
    print(f"10.5 + 20.3 = {result}")
