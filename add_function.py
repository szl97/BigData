if __name__ == "__main__":
    # 测试用例
    print(f"add(2, 3) = {add(2, 3)}")
    print(f"add(10, 20) = {add(10, 20)}")
    print(f"add(-5, 8) = {add(-5, 8)}")
    print(f"add(3.5, 2.5) = {add(3.5, 2.5)}")
